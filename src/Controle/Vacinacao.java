
package Controle;


public class Vacinacao {
    
    //Atributos
    
    private int codigoVacinacao;
    private Vacina codigoVacina;
    private Animal codigoAnimal;

    public int getCodigoVacinacao() {
        return codigoVacinacao;
    }

    public void setCodigoVacinacao(int codigoVacinacao) {
        this.codigoVacinacao = codigoVacinacao;
    }

    public Vacina getCodigoVacina() {
        return codigoVacina;
    }

    public void setCodigoVacina(Vacina codigoVacina) {
        this.codigoVacina = codigoVacina;
    }

    public Animal getCodigoAnimal() {
        return codigoAnimal;
    }

    public void setCodigoAnimal(Animal codigoAnimal) {
        this.codigoAnimal = codigoAnimal;
    }
    
    
    
}
